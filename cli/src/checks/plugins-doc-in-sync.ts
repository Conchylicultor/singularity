import { existsSync, readFileSync } from "fs";
import { join } from "path";
import type { Check } from "./types";
import { renderFullDoc, renderPluginDocs } from "../docgen";

async function getRoot(): Promise<string> {
  const proc = Bun.spawn(["git", "rev-parse", "--show-toplevel"], {
    stdout: "pipe",
    stderr: "pipe",
  });
  return (await new Response(proc.stdout).text()).trim();
}

const BEGIN = "<!-- AUTOGENERATED:BEGIN";
const END = "<!-- AUTOGENERATED:END -->";

export const pluginsDocInSync: Check = {
  id: "plugins-doc-in-sync",
  description: "plugins/CLAUDE.md matches the current plugin source",
  async run() {
    const root = await getRoot();
    const file = join(root, "plugins", "CLAUDE.md");
    if (!existsSync(file)) {
      return {
        ok: false,
        message: "plugins/CLAUDE.md is missing",
        hint: "Run `./singularity build` to generate it.",
      };
    }
    const existing = readFileSync(file, "utf8");
    const body = renderPluginDocs({ root });

    let expected: string;
    if (existing.includes(BEGIN) && existing.includes(END)) {
      const beginLine = existing.slice(existing.indexOf(BEGIN));
      const beginHead = beginLine.slice(0, beginLine.indexOf("\n"));
      const before = existing.slice(0, existing.indexOf(BEGIN) + beginHead.length);
      const after = existing.slice(existing.indexOf(END));
      expected = `${before}\n\n${body}\n${after}`;
    } else {
      expected = renderFullDoc({ root });
    }

    if (existing !== expected) {
      return {
        ok: false,
        message: "plugins/CLAUDE.md is out of sync with plugin source",
        hint: "Run `./singularity build` and commit the regenerated file.",
      };
    }
    return { ok: true };
  },
};
