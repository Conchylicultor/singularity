/**
 * Walk plugins/ and return one PluginInfo per discovered plugin (flat list,
 * including nested sub-plugins). Used by build to derive the central routing
 * manifest.
 */
export function collectAllPlugins(root: string): PluginInfo[] {
  const pluginsRoot = resolve(root, "plugins");
  const dirs = findAllPluginDirs(pluginsRoot);
  return dirs.map((d) => collectPlugin(d, pluginsRoot));
}

export type { PluginInfo };

function computeReverseIndexes(byDir: Map<string, PluginInfo>): void {
  const byName = new Map<string, PluginInfo>();
  for (const info of byDir.values()) byName.set(info.name, info);

  // 1. importedBy — invert apiUses (server + central runtime imports)
  for (const importer of byDir.values()) {
    const referenced = new Set<string>();
    for (const u of [...importer.apiUses, ...importer.centralApiUses]) {
      referenced.add(u.split(".")[0]!);
    }
    for (const targetName of referenced) {
      const target = byName.get(targetName);
      if (!target || target === importer) continue;
      if (!target.importedBy.includes(importer.name)) {
        target.importedBy.push(importer.name);
      }
    }
  }

  // 2. slotContributors — invert contributions; owner is the plugin whose
  // `definedSlots` declares the contribution's group name (e.g. `Shell.Sidebar`
  // → group `Shell`, owned by whichever plugin defined a Shell group).
  const slotGroupToOwner = new Map<string, PluginInfo>();
  for (const info of byDir.values()) {
    for (const slot of info.definedSlots) {
      if (!slotGroupToOwner.has(slot.groupName)) {
        slotGroupToOwner.set(slot.groupName, info);
      }
    }
  }
  for (const contributor of byDir.values()) {
    const groups = new Set<string>();
    for (const c of contributor.contributions) {
      const head = c.slot.split(".")[0];
      if (head) groups.add(head);
    }
    for (const group of groups) {
      const owner = slotGroupToOwner.get(group);
      if (!owner || owner === contributor) continue;
      if (!owner.slotContributors.includes(contributor.name)) {
        owner.slotContributors.push(contributor.name);
      }
    }
  }

  // 3. endpointCallers — best-effort: build an `/api/<prefix>` → owner map from
  // each plugin's registered HTTP routes, then grep every plugin's source for
  // those literals.
  const apiPrefixToOwner = new Map<string, PluginInfo>();
  for (const info of byDir.values()) {
    for (const route of [...info.httpRoutes, ...info.centralHttpRoutes]) {
      // route is like "GET /api/tasks/:id" — strip method and extract the
      // first path segment after "/api/".
      const pathMatch = route.match(/^\S+\s+\/api\/([A-Za-z0-9_-]+)/);
      if (!pathMatch) continue;
      const prefix = pathMatch[1]!;
      if (!apiPrefixToOwner.has(prefix)) {
        apiPrefixToOwner.set(prefix, info);
      }
    }
  }
  if (apiPrefixToOwner.size > 0) {
    const prefixes = [...apiPrefixToOwner.keys()];
    const escaped = prefixes.map((p) => p.replace(/[.*+?^${}()|[\]\\]/g, "\\$&"));
    const re = new RegExp(`\\/api\\/(${escaped.join("|")})(?![A-Za-z0-9_-])`, "g");
    for (const caller of byDir.values()) {
      const files: string[] = [];
      for (const sub of ["web", "server", "central"]) {
        const subDir = join(caller.dir, sub);
        if (existsSync(subDir)) walkFiles(subDir, files);
      }
      const hit = new Set<string>();
      for (const f of files) {
        const src = readIfExists(f);
        if (!src) continue;
        let m: RegExpExecArray | null;
        re.lastIndex = 0;
        while ((m = re.exec(src))) hit.add(m[1]!);
      }
      for (const prefix of hit) {
        const owner = apiPrefixToOwner.get(prefix);
        if (!owner || owner === caller) continue;
        if (!owner.endpointCallers.includes(caller.name)) {
          owner.endpointCallers.push(caller.name);
        }
      }
    }
  }

  for (const info of byDir.values()) {
    info.importedBy.sort();
    info.slotContributors.sort();
    info.endpointCallers.sort();
  }

  // 4. entityExtensions — detect defineExtension() calls; cross-link to parent plugin
  const pluginVarToTable = new Map<string, Map<string, string>>();
  for (const info of byDir.values()) {
    pluginVarToTable.set(info.name, parseTableNamesFromDbFiles(info.dbFiles));
  }
  const pluginModuleRe = /@plugins\/([^/"'`]+)\/(?:server|central|shared)/;
  for (const info of byDir.values()) {
    for (const ref of parseEntityExtensionCalls(info.dbFiles)) {
      const pluginMatch = ref.parentModule.match(pluginModuleRe);
      if (!pluginMatch) continue;
      const parentPluginName = pluginMatch[1]!;
      const parentTableName =
        (pluginVarToTable.get(parentPluginName) ?? new Map()).get(ref.parentVarName) ?? "";
      const tableName = parentTableName
        ? `${parentTableName}_ext_${ref.extName}`
        : `${parentPluginName}_ext_${ref.extName}`;
      if (!info.entityExtensions.some((e) => e.tableName === tableName)) {
        info.entityExtensions.push({ parentPlugin: parentPluginName, extName: ref.extName, tableName });
      }
      const parentPlugin = byName.get(parentPluginName);
      if (parentPlugin && !parentPlugin.extendedBy.some((e) => e.tableName === tableName)) {
        parentPlugin.extendedBy.push({ childPlugin: info.name, extName: ref.extName, tableName });
      }
    }
  }
  for (const info of byDir.values()) {
    info.entityExtensions.sort((a, b) => a.tableName.localeCompare(b.tableName));
    info.extendedBy.sort((a, b) => a.tableName.localeCompare(b.tableName));
  }
}

interface PluginTree {
  byDir: Map<string, PluginInfo>;
  roots: PluginInfo[];
}

function buildPluginTree(root: string): PluginTree {
  const pluginsRoot = resolve(root, "plugins");
  const dirs = findAllPluginDirs(pluginsRoot);
  const byDir = new Map<string, PluginInfo>();
  for (const d of dirs) byDir.set(d, collectPlugin(d, pluginsRoot));

  computeReverseIndexes(byDir);

  const roots: PluginInfo[] = [];
  for (const info of byDir.values()) {
    let parent = info.parentDir;
    while (parent && !byDir.has(parent)) {
      const rel = relative(pluginsRoot, parent);
      const segs = rel.split(/[\\/]+/);
      if (segs.length >= 3 && segs[segs.length - 2] === "plugins") {
        parent = join(pluginsRoot, ...segs.slice(0, segs.length - 2));
      } else {
        parent = null;
      }
    }
    if (parent && byDir.has(parent)) {
      byDir.get(parent)!.children.push(info);
    } else {
      roots.push(info);
    }
  }

  const sortRec = (list: PluginInfo[]) => {
    list.sort((a, b) => a.name.localeCompare(b.name));
    for (const c of list) sortRec(c.children);
  };
  sortRec(roots);

  return { byDir, roots };
}

function renderTreeBody(roots: PluginInfo[], root: string, mode: RenderMode): string {
  const lines: string[] = [];
  for (let i = 0; i < roots.length; i++) {
    if (i > 0) lines.push("");
    lines.push(...renderPluginTree(roots[i]!, 0, root, mode));
  }
  return lines.join("\n") + "\n";
}

const COMPACT_HEADER =
  "# Plugins (compact)\n\n" +
  "Slim, always-loaded index of every plugin. Shows only `name — description`; load-bearing infrastructure plugins are marked `[load-bearing]`. Read [`plugins-details.md`](./plugins-details.md) for the full reference, or open the per-plugin `CLAUDE.md` when working inside a specific plugin.\n\n";

const DETAILS_HEADER =
  "# Plugins (details)\n\n" +
  "Full reference for every plugin. Read this on demand (e.g. before writing a helper, to check whether one already exists). The slim always-loaded index is [`plugins-compact.md`](./plugins-compact.md).\n\n";

export function renderCompactDoc({ root }: GenerateDocsOptions): string {
  return renderCompactDocFromTree(buildPluginTree(root), root);
}

export function renderDetailsDoc({ root }: GenerateDocsOptions): string {
  return renderDetailsDocFromTree(buildPluginTree(root), root);
}

/**
 * Returns the AUTOGEN body for a single plugin's CLAUDE.md — the same body as
 * the plugin's section in details.md but without the bullet header (so it can
 * sit under the file's own `# <Title>` heading and hand-written prose).
 */
function renderPluginClaudeAutogen(p: PluginInfo, root: string): string {
  const lines: string[] = [];
  lines.push("## Plugin reference");
  lines.push("");
  if (p.description) lines.push(`- Description: ${p.description}`);
  if (p.loadBearing) lines.push("- Load-bearing: yes");
  renderPluginBody(p, "", root, lines);
  if (p.children.length > 0) {
    lines.push("- Sub-plugins:");
    for (const c of p.children) {
      const cdesc = c.description ? ` — ${c.description}` : "";
      lines.push(`  - **\`${c.name}\`**${cdesc}`);
    }
  }
  return lines.join("\n") + "\n";
}

/**
 * Merges the freshly-rendered AUTOGEN block into an existing CLAUDE.md,
 * preserving any hand-written prose above the BEGIN marker. If the existing
 * file has no AUTOGEN fence, the block is appended at the end. If the file
 * does not exist, a minimal stub (`# <name>\n\n` + AUTOGEN block) is created.
 */
function renderPluginClaudeMd(p: PluginInfo, existing: string | null, root: string): string {
  const autogen = renderPluginClaudeAutogen(p, root);
  const block = `${BEGIN}\n\n${autogen}\n${END}\n`;

  if (existing === null) {
    return `# ${p.name}\n\n${block}`;
  }
  const beginIdx = existing.indexOf(BEGIN);
  if (beginIdx < 0) {
    const sep = existing.endsWith("\n") ? "\n" : "\n\n";
    return `${existing}${sep}${block}`;
  }
  const endIdx = existing.indexOf(END, beginIdx);
  if (endIdx < 0) {
    // Truncated file — replace from BEGIN to EOF with the new block.
    return `${existing.slice(0, beginIdx)}${block}`;
  }
  const after = existing.slice(endIdx + END.length);
  // Drop a single trailing newline immediately after END (will be re-added by `block`).
  const trimmedAfter = after.startsWith("\n") ? after.slice(1) : after;
  return `${existing.slice(0, beginIdx)}${block}${trimmedAfter}`;
}

export function pluginCompactDocPath(root: string): string {
  return join(root, "docs", "plugins-compact.md");
}

export function pluginDetailsDocPath(root: string): string {
  return join(root, "docs", "plugins-details.md");
}

export function pluginClaudeMdPath(p: PluginInfo): string {
  return join(p.dir, "CLAUDE.md");
}

/** Pre-restructure single-doc path; kept only so we can clean it up on first run. */
function legacyPluginDocsPath(root: string): string {
  return join(root, "docs", "plugins.md");
}

export async function generatePluginDocs({ root }: GenerateDocsOptions): Promise<void> {
  const tree = buildPluginTree(root);

  const writeIfChanged = (file: string, next: string) => {
    const existing = readIfExists(file) ?? "";
    if (next !== existing) writeFileSync(file, next);
  };

  // Write the two top-level docs.
  writeIfChanged(pluginCompactDocPath(root), renderCompactDocFromTree(tree, root));
  writeIfChanged(pluginDetailsDocPath(root), renderDetailsDocFromTree(tree, root));

  // Write a CLAUDE.md for every plugin (including nested sub-plugins).
  for (const info of tree.byDir.values()) {
    const file = pluginClaudeMdPath(info);
    const existing = readIfExists(file);
    const next = renderPluginClaudeMd(info, existing, root);
    if (next !== existing) writeFileSync(file, next);
  }

  // One-shot cleanup: remove the legacy single-doc artifact if it lingers.
  const legacy = legacyPluginDocsPath(root);
  if (existsSync(legacy)) {
    try {
      unlinkSync(legacy);
    } catch {
      // Best-effort; if the user has a writeable repo, this should never fail.
    }
  }
}

function renderCompactDocFromTree(tree: PluginTree, root: string): string {
  const body = renderTreeBody(tree.roots, root, "compact");
  return `${COMPACT_HEADER}${BEGIN}\n\n${body}\n${END}\n`;
}

function renderDetailsDocFromTree(tree: PluginTree, root: string): string {
  const body = renderTreeBody(tree.roots, root, "detail");
  return `${DETAILS_HEADER}${BEGIN}\n\n${body}\n${END}\n`;
}

