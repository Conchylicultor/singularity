import { existsSync, readdirSync, readFileSync, writeFileSync } from "fs";
import { join, resolve } from "path";
import { buildPluginTree } from "@plugins/plugin-meta/plugins/plugin-tree/core";

export type Runtime = "web" | "server" | "central";

interface RuntimeConfig {
  registryFile: string;
  generatedFile: string;
  typeName: string;
  typeImport: string;
}

const RUNTIMES: Record<Runtime, RuntimeConfig> = {
  web: {
    registryFile: "web/src/plugins.ts",
    generatedFile: "web/src/plugins.generated.ts",
    typeName: "PluginDefinition",
    typeImport: `import type { PluginDefinition } from "@plugins/framework/plugins/web-sdk/core";`,
  },
  server: {
    registryFile: "server/src/plugins.ts",
    generatedFile: "server/src/plugins.generated.ts",
    typeName: "ServerPluginDefinition",
    typeImport: `import type { ServerPluginDefinition } from "./types";`,
  },
  central: {
    registryFile: "central/src/plugins.ts",
    generatedFile: "central/src/plugins.generated.ts",
    typeName: "CentralPluginDefinition",
    typeImport: `import type { CentralPluginDefinition } from "./types";`,
  },
};

const HEADER = [
  "// DO NOT EDIT — autogenerated by `./singularity build`",
  "// (see tooling/src/plugin-registry-gen.ts).",
  "//",
  "// Adding/removing a plugin: create or delete the plugin's `<runtime>/index.ts`",
  "// and rebuild — this file regenerates from the filesystem. The",
  "// `plugins-registry-in-sync` check fails on drift.",
].join("\n");

/**
 * Derive a deterministic camelCase import name from a plugin's path relative
 * to `plugins/`. Drops the literal `plugins` connector segments and appends
 * `Plugin`. Example: `conversations/plugins/conversation-view/plugins/code`
 * → `conversationsConversationViewCodePlugin`.
 */
function importNameFor(relPath: string): string {
  const segments = relPath.split(/[\\/]+/).filter((s) => s !== "plugins");
  const camel = segments
    .map((seg, i) => {
      const parts = seg.split("-").filter(Boolean);
      const head =
        i === 0
          ? parts[0]!
          : parts[0]!.charAt(0).toUpperCase() + parts[0]!.slice(1);
      const tail = parts
        .slice(1)
        .map((p) => p.charAt(0).toUpperCase() + p.slice(1))
        .join("");
      return head + tail;
    })
    .join("");
  return `${camel}Plugin`;
}

interface Entry {
  importName: string;
  importPath: string; // `@plugins/<rel>/<runtime>`
  hierarchyPath?: string; // derived from hierarchyId (dots → slashes)
}

/**
 * Some `<runtime>/index.ts` files are public API barrels (e.g.
 * `plugins/infra/plugins/secrets/server/index.ts` exposes helpers other
 * server-side plugins import) and not registered plugin definitions. The
 * difference: a registered plugin has `export default <PluginDefinition>`.
 * Filter on that so barrels-only modules don't leak into the registry.
 */
function hasDefaultExport(file: string): boolean {
  const src = readFileSync(file, "utf8");
  return (
    /(^|\n)\s*export\s+default\b/.test(src) ||
    /(^|\n)\s*export\s*\{[^}]*\bdefault\b[^}]*\}/.test(src)
  );
}

function collectEntries(root: string, runtime: Runtime): Entry[] {
  const pluginsRoot = resolve(root, "plugins");
  const tree = buildPluginTree(pluginsRoot);
  const entries: Entry[] = [];
  for (const node of tree.byDir.values()) {
    const indexFile = join(node.dir, runtime, "index.ts");
    if (!existsSync(indexFile)) continue;
    if (!hasDefaultExport(indexFile)) continue;
    entries.push({
      importName: importNameFor(node.path),
      importPath: `@plugins/${node.path}/${runtime}`,
      hierarchyPath: node.hierarchyId.replaceAll(".", "/"),
    });
  }
  entries.sort((a, b) => a.importPath.localeCompare(b.importPath));
  return entries;
}

// ── Server dependency graph ─────────────────────────────────────────

function walkTsFiles(dir: string, out: string[]): void {
  let entries;
  try {
    entries = readdirSync(dir, { withFileTypes: true });
  } catch {
    return;
  }
  for (const e of entries) {
    const p = join(dir, e.name);
    if (e.isDirectory()) {
      if (e.name === "node_modules" || e.name === "plugins") continue;
      walkTsFiles(p, out);
    } else if (e.isFile() && /\.tsx?$/.test(e.name)) {
      out.push(p);
    }
  }
}

const IMPORT_FROM_RE = /from\s*["']([^"']+)["']/g;

function collectServerImportPaths(pluginDir: string, runtime: string): Set<string> {
  const serverDir = join(pluginDir, runtime);
  const files: string[] = [];
  walkTsFiles(serverDir, files);
  const paths = new Set<string>();
  for (const f of files) {
    const src = readFileSync(f, "utf8");
    let m: RegExpExecArray | null;
    IMPORT_FROM_RE.lastIndex = 0;
    while ((m = IMPORT_FROM_RE.exec(src))) {
      const mod = m[1]!;
      if (mod.startsWith("@plugins/") && mod.endsWith(`/${runtime}`)) {
        paths.add(mod);
      }
    }
  }
  return paths;
}

/**
 * Build a map from importName → list of importNames it depends on, derived
 * from cross-plugin server/central import edges. Only includes entries with
 * at least one dependency.
 */
function buildDependsOn(
  root: string,
  entries: Entry[],
  runtime: "server" | "central",
): Map<string, string[]> {
  const importPathToName = new Map<string, string>();
  for (const e of entries) importPathToName.set(e.importPath, e.importName);

  const pluginsRoot = resolve(root, "plugins");
  const result = new Map<string, string[]>();

  for (const e of entries) {
    const pluginDir = join(pluginsRoot, e.importPath.replace(`@plugins/`, "").replace(`/${runtime}`, ""));
    const importPaths = collectServerImportPaths(pluginDir, runtime);
    const deps: string[] = [];
    for (const imp of importPaths) {
      const depName = importPathToName.get(imp);
      if (depName && depName !== e.importName) deps.push(depName);
    }
    if (deps.length > 0) {
      deps.sort();
      result.set(e.importName, deps);
    }
  }
  return result;
}

export function renderPluginRegistry(opts: { root: string; runtime: Runtime }): string {
  const cfg = RUNTIMES[opts.runtime];
  const entries = collectEntries(opts.root, opts.runtime);
  const lines: string[] = [];
  lines.push(HEADER);
  lines.push("");

  if (opts.runtime === "web") {
    lines.push(`import type { PluginDefinition } from "@plugins/framework/plugins/web-sdk/core";`);
    lines.push("");
    lines.push("export interface PluginEntry {");
    lines.push("  name: string;");
    lines.push("  hierarchyPath?: string;");
    lines.push("  loader: () => Promise<{ default: PluginDefinition }>;");
    lines.push("}");
    lines.push("");
    lines.push("export const pluginEntries: PluginEntry[] = [");
    for (const e of entries) {
      const name = e.importPath.replace(/^@plugins\//, "").replace(/\/web$/, "");
      lines.push(`  { name: ${JSON.stringify(name)}, hierarchyPath: ${JSON.stringify(e.hierarchyPath)}, loader: () => import(${JSON.stringify(e.importPath)}) },`);
    }
    lines.push("];");
    lines.push("");
    return lines.join("\n");
  }

  lines.push(cfg.typeImport);
  for (const e of entries) {
    lines.push(`import ${e.importName} from "${e.importPath}";`);
  }

  // eslint-disable-next-line @typescript-eslint/no-unnecessary-condition -- forward-compat guard; more runtimes may be added
  if (opts.runtime === "server" || opts.runtime === "central") {
    const deps = buildDependsOn(opts.root, entries, opts.runtime);
    if (deps.size > 0) {
      lines.push("");
      const cast = `as ${cfg.typeName}`;
      for (const e of entries) {
        const d = deps.get(e.importName);
        if (!d) continue;
        lines.push(`(${e.importName} ${cast}).dependsOn = [${d.join(", ")}];`);
      }
    }
    lines.push("");
    const cast = `as ${cfg.typeName}`;
    for (const e of entries) {
      if (e.hierarchyPath) {
        lines.push(`(${e.importName} ${cast})._hierarchyPath = ${JSON.stringify(e.hierarchyPath)};`);
      }
    }
  }

  lines.push("");
  lines.push(`export const plugins: ${cfg.typeName}[] = [`);
  for (const e of entries) {
    lines.push(`  ${e.importName},`);
  }
  lines.push("];");
  lines.push("");
  return lines.join("\n");
}

export function pluginRegistryPath(root: string, runtime: Runtime): string {
  return join(root, RUNTIMES[runtime].generatedFile);
}

export async function generatePluginRegistry(opts: { root: string }): Promise<void> {
  for (const runtime of Object.keys(RUNTIMES) as Runtime[]) {
    const file = pluginRegistryPath(opts.root, runtime);
    const next = renderPluginRegistry({ root: opts.root, runtime });
    const existing = existsSync(file) ? readFileSync(file, "utf8") : "";
    if (next !== existing) writeFileSync(file, next);
  }
}
