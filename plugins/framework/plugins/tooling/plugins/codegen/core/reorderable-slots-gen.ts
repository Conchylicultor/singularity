import { existsSync, readFileSync, writeFileSync } from "fs";
import { join, resolve } from "path";
import { buildEnrichedTree } from "./docgen";
import { buildPluginTree } from "@plugins/plugin-meta/plugins/plugin-tree/core";
import {
  setDefaultOriginAnnotationsPreparer,
  setDefaultOriginDefaultsPreparer,
  type OriginAnnotationsProvider,
} from "./config-origin-gen";
import { getFacet } from "@plugins/plugin-meta/plugins/facets/core";
import { collectRenderSlotsStatic } from "./reorderable-slots-scan";
import { contributionsFacetDef } from "@plugins/plugin-meta/plugins/facets/plugins/contributions/core";
import { asPluginId } from "@plugins/framework/plugins/plugin-id/core";
import { computeDisabledIds } from "./disabled-ids";
import type { ConfigDescriptor } from "@plugins/config_v2/core";

/**
 * Generates the reorderable-slots manifest consumed by the reorder plugin and
 * the per-slot contribution catalog injected into generated config origins.
 *
 * The manifest is a pure-data bridge: the live server can't see web-slot
 * contributions, but this build-time codegen (via the facets pipeline docgen
 * already uses) can. reorder reads `reorderableSlots` from
 * `plugins/reorder/shared/reorderable-slots.generated.ts` on both runtimes and
 * registers one config_v2 directive per slot.
 */

// The output mirrors the placeholder committed at
// plugins/reorder/shared/reorderable-slots.generated.ts.
const MANIFEST_REL_PATH =
  "plugins/reorder/shared/reorderable-slots.generated.ts";

const MANIFEST_HEADER = [
  "// DO NOT EDIT — autogenerated by `./singularity build`",
  "// (see plugins/framework/plugins/tooling/plugins/codegen/core/reorderable-slots-gen.ts).",
  "//",
  "// The list of reorderable render slots (those whose `.Render` applies the",
  "// reorder middleware, i.e. every `defineRenderSlot`; mount slots are excluded). Each",
  "// entry's `pluginId` is the *defining* plugin's id (its tree path), so reorder",
  "// registers the slot's config_v2 directive under `config/<pluginId>/`.",
  "//",
  "// The `reorderable-slots-in-sync` check fails on drift.",
].join("\n");

export interface ReorderableSlotEntry {
  slotId: string;
  pluginId: string;
}

interface CatalogItem {
  entryKey: string;
}

interface ReorderableSlotsData {
  slots: ReorderableSlotEntry[];
  /** slotId -> ordered catalog of available contributions. */
  catalog: Map<string, CatalogItem[]>;
}

/**
 * The reorderable-slot SET — deterministic static scan, BARREL-FREE.
 *
 * Collects every reorderable render slot, keyed to its DEFINING plugin's
 * hierarchy path, via the STATIC, factory-aware source scanner
 * (`collectRenderSlotsStatic`). This is a pure function of committed source: it
 * never imports a barrel, so the slot set is identical in every environment (the
 * old live-barrel walk made it depend on which barrels happened to evaluate,
 * which dirtied `main` on the post-push auto-build). Already sorted by slotId and
 * deduped (first definer wins) by the collector.
 *
 * This is the ONLY input `renderManifest` needs, and it MUST stay barrel-free:
 * the manifest it feeds is imported by plugin barrels at module-load, so it has
 * to be regenerated BEFORE the first barrel import freezes the ESM cache (a
 * later disk write cannot invalidate it). The barrel-importing catalog lives in
 * `collectReorderableSlots`, consumed only by the origin-preparers — which run
 * lazily during `generateConfigOrigins`, after barrels are primed.
 */
async function collectReorderableSlotSet(
  root: string,
): Promise<ReorderableSlotEntry[]> {
  const tree = await buildPluginTree(resolve(root, "plugins"), {
    skipBarrelImport: true,
  });
  // A slot owned by a disabled plugin never registers at runtime (the owner's
  // barrel is omitted from the registry); its config_v2 directive would orphan
  // user overrides. Drop slots whose DEFINING plugin is in the disabled closure.
  // `classifyEdges` runs on this barrel-free tree (facets are populated), so the
  // set is deterministic and matches the registry phase.
  const disabled = computeDisabledIds(tree);
  return collectRenderSlotsStatic(tree)
    .filter(({ pluginId }) => !disabled.has(asPluginId(pluginId)))
    .map(({ slotId, pluginId }) => ({ slotId, pluginId }));
}

/**
 * Walk the enriched plugin tree (reusing docgen's cached build) and collect:
 *  1. the reorderable-slot set (via `collectReorderableSlotSet`).
 *  2. the contribution catalog for each such slot, from the contributions facet
 *     (a RUNTIME read — needs barrel imports; only the catalog/origin defaults
 *     need it, not the reorderable-slot SET). This is why this function is used
 *     ONLY by the origin-preparers, which run lazily after barrels are primed.
 */
async function collectReorderableSlots(
  root: string,
): Promise<ReorderableSlotsData> {
  const tree = await buildEnrichedTree(root);
  // Drop catalog entries contributed by disabled plugins: their barrels never
  // load, so listing them in an enabled slot's materialized default would point
  // the origin hash at contributions that don't register. (The slot SET itself
  // is already disabled-filtered inside `collectReorderableSlotSet`.)
  const disabled = computeDisabledIds(tree);

  // (1) The reorderable-slot set — deterministic static scan.
  const slots = await collectReorderableSlotSet(root);
  const reorderableIds = new Set(slots.map((s) => s.slotId));

  // (2) Catalog: every runtime contribution targeting a reorderable slot.
  const catalog = new Map<string, CatalogItem[]>();
  for (const node of tree.byDir.values()) {
    if (disabled.has(node.id)) continue;
    const data = getFacet(node, contributionsFacetDef);
    if (!data) continue;
    for (const c of data.runtime) {
      if (!reorderableIds.has(c.slotId)) continue;
      if (!c.id) continue;
      const entryKey = c.pluginId ? `${c.pluginId}:${c.id}` : c.id;
      let items = catalog.get(c.slotId);
      if (!items) {
        items = [];
        catalog.set(c.slotId, items);
      }
      items.push({ entryKey });
    }
  }

  return { slots, catalog };
}

function renderManifest(slots: ReorderableSlotEntry[]): string {
  const lines: string[] = [];
  lines.push(MANIFEST_HEADER);
  lines.push("");
  lines.push("export interface ReorderableSlot {");
  lines.push("  slotId: string;");
  lines.push("  pluginId: string;");
  lines.push("}");
  lines.push("");
  if (slots.length === 0) {
    lines.push("export const reorderableSlots: ReorderableSlot[] = [];");
  } else {
    lines.push("export const reorderableSlots: ReorderableSlot[] = [");
    for (const s of slots) {
      lines.push(
        `  { slotId: ${JSON.stringify(s.slotId)}, pluginId: ${JSON.stringify(s.pluginId)} },`,
      );
    }
    lines.push("];");
  }
  lines.push("");
  return lines.join("\n");
}

/** Path to the committed manifest file. */
export function reorderableSlotsManifestPath(root: string): string {
  return join(root, MANIFEST_REL_PATH);
}

/** Render the manifest file contents in-memory (used by the in-sync check). */
export async function renderReorderableSlotsManifest(
  root: string,
): Promise<string> {
  // Barrel-free: the manifest only needs the static slot SET, NOT the
  // barrel-imported catalog. This lets the manifest be regenerated before the
  // first barrel import (see `collectReorderableSlotSet`).
  const slots = await collectReorderableSlotSet(root);
  return renderManifest(slots);
}

/**
 * Slim origin-annotations provider: the order lives in the `items` value (a
 * materialized `ReorderTree`) and each entry is a fully-qualified `entryKey`
 * (`pluginId:id`), so the comments are just a hand-edit format note for the
 * extension node types (a bare string is the terse form of an item) — the
 * per-entry label map would only restate the already-explicit keys.
 */
function buildOriginAnnotationsProvider(
  catalog: Map<string, CatalogItem[]>,
): OriginAnnotationsProvider {
  return (descriptor: ConfigDescriptor): string[] => {
    // The directive descriptor's `name` is the slotId.
    const items = catalog.get(descriptor.name);
    if (!items || items.length === 0) return [];
    return [
      'Hide: { "item": "<key>", "hidden": true }',
      'Spacer (gap): { "type": "spacer", "id": "<unique-id>" }',
      'Group: { "type": "header", "label": "<title>", "collapsed": false, "items": [ "<key>", … ] }',
    ];
  };
}

/**
 * Side-effecting registration: installs the reorder catalog as the process-wide
 * default origin-annotations preparer. Imported by BOTH the build step and the
 * `config-origins-in-sync` check, so both processes resolve the same comments.
 *
 * Returns a no-op for descriptors that aren't reorder directives (their slotId
 * isn't in the catalog) — keeping the provider generic.
 */
setDefaultOriginAnnotationsPreparer(async (root: string) => {
  const { catalog } = await collectReorderableSlots(root);
  return buildOriginAnnotationsProvider(catalog);
});

/**
 * Side-effecting registration: installs the reorder catalog as the process-wide
 * default origin-DEFAULTS preparer. The materialized default for each reorderable
 * slot's `items` field is the full ordered catalog of `entryKey`s (bare strings —
 * terse). This makes the origin hash reflect the live catalog: adding/removing a
 * contribution shifts the hash → committed overrides go stale and
 * `config-origins-in-sync` blocks push until reconciled (the agent forcing
 * function). Returns `undefined` for descriptors whose slotId isn't in the
 * catalog, so non-reorder descriptors fall back to `descriptor.defaults`.
 */
setDefaultOriginDefaultsPreparer(async (root: string) => {
  const { catalog } = await collectReorderableSlots(root);
  return (descriptor: ConfigDescriptor) => {
    const items = catalog.get(descriptor.name);
    return items ? { items: items.map((c) => c.entryKey) } : undefined;
  };
});

/** Regenerate `reorderable-slots.generated.ts` if it drifted. */
export async function generateReorderableSlots(opts: {
  root: string;
}): Promise<void> {
  const next = await renderReorderableSlotsManifest(opts.root);
  const file = reorderableSlotsManifestPath(opts.root);
  const existing = existsSync(file) ? readFileSync(file, "utf8") : "";
  if (next !== existing) writeFileSync(file, next);
}
